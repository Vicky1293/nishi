package practicekarvikram;

public class UnaryOperator1 {

	public static void main(String[] args) {
		int a = 1;
		System.out.println(a++);
		System.out.println(a); 
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		System.out.println(++a);
		System.out.println(a++);
		System.out.println(a);
	}

}
